CREATE TABLE swimmers
(
swimmerID VARCHAR(20),
swimmerFirstName VARCHAR(20),
swimmerMiddleInitial CHAR(1),
swimmerLastName VARCHAR(20),
birthday DATE,
swimGroup VARCHAR(10)
);

CREATE TABLE families
(
familyID INT(10) NOT NULL AUTO_INCREMENT,
PRIMARY KEY (familyID),
email VARCHAR(30),
password VARCHAR(40),
parentFirstName VARCHAR(20),
parentLastName VARCHAR(20),
yearJoined INT(4),
streetAddress VARCHAR(30),
city VARCHAR(20),
state VARCHAR(20),
zip VARCHAR(20),
homePhone VARCHAR(20),
workPhone VARCHAR(20),
cellPhone VARCHAR(20),
accessLevel CHAR(1),
overallAFD DEC(8,2),
currentAFD DEC(8,2)
);

CREATE TABLE IF NOT EXISTS `order` (
  `orderID` int(11) NOT NULL AUTO_INCREMENT,
  `familyID` int(11) NOT NULL,
  `datePlaced` date NOT NULL,
  `dateConfirmed` date DEFAULT NULL,
  `totalPaid` double(8,2) NOT NULL,
  PRIMARY KEY (`orderID`)
) ENGINE=MyISAM  DEFAULT CHARSET=latin1 AUTO_INCREMENT=25 ;


CREATE TABLE IF NOT EXISTS `itemizedorder` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `cardID` int(11) NOT NULL,
  `orderID` int(11) NOT NULL,
  `quantityOrdered` int(11) NOT NULL,
  `cost` double NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=MyISAM  DEFAULT CHARSET=latin1 AUTO_INCREMENT=0 ;


